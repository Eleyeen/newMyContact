package com.example.mycontacts.dataModel;

public class GroupDataModel {
    String groupName;
    String groupContactNum;
    String groupContactName;

    public GroupDataModel() {
        this.groupName = groupName;
        this.groupContactNum = groupContactNum;
        this.groupContactName = groupContactName;
    }

    public String getGroupName() {
        return groupName;
    }

    public void setGroupName(String groupName) {
        this.groupName = groupName;
    }

    public String getGroupContactNum() {
        return groupContactNum;
    }

    public void setGroupContactNum(String groupContactNum) {
        this.groupContactNum = groupContactNum;
    }

    public String getGroupContactName() {
        return groupContactName;
    }

    public void setGroupContactName(String groupContactName) {
        this.groupContactName = groupContactName;
    }
}